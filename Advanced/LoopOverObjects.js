//Keys:
const car2 = {
    speed: 200,
    color: "red"
}
console.log(Object.keys(car2)); // ['speed','color']

//Values:
const car3 = {
    speed: 300,
    color: "yellow"
}
console.log(Object.values(car3)); // [300, 'yellow']

//Entries:
const car4 = {
    speed: 400,
    color: 'magenta'
} 
console.log(Object.entries(car4)); //[ ['speed', 400], ['color', 'magenta'] ]